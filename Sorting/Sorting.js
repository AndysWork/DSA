/* 
Sorting - the process of arranging items systematically.
Sorting is a fundamental operation in computer science.

Sorting algorithms are used to optimize the performance of data stored in a computer's memory.

Sorting Algorithms:
1. Bubble Sort - repeatedly steps through the list, compares adjacent elements and 
swaps them if they are in the wrong order.
2. Selection Sort - repeatedly finds the minimum element from the unsorted part of the list and
3. Insertion Sort - builds the final sorted list one item at a time.
4. Merge Sort - divides the array into two halves, sorts the two halves and then merges them.
5. Quick Sort - picks an element as pivot and partitions the given array around the picked pivot.
6. Heap Sort - builds a heap tree and then sorts the array.
7. Counting Sort - sorts the elements of an array by counting the number of occurrences of 
each unique element.
8. Radix Sort - sorts the elements by first grouping the individual digits of the same place value.
9. Bucket Sort - divides the array into a number of buckets, each bucket is then sorted individually.
10. Shell Sort - sorts the elements far apart from each other and then reduces the gap between them.
11. Cocktail Sort - a variation of Bubble Sort, sorts the list in both directions.
12. Comb Sort - improves on Bubble Sort by using a gap sequence to remove turtles.
13. Pigeonhole Sort - suitable for sorting lists where the number of elements and the number 
of possible key values are approximately the same.
14. Cycle Sort - minimizes the number of memory writes to sort the list.
15. Bitonic Sort - first produces a bitonic sequence and then sorts it.
16. Pancake Sorting - sorts the list by selecting the largest element and flipping the list.
17. Binary Insertion Sort - uses binary search to find the proper location to insert the selected item.
18. Bogo Sort - repeatedly shuffles the list until it is sorted.
19. Gnome Sort - sorts the list by moving an element to its proper place.
20. Sleep Sort - sorts the list by creating a separate thread for each element.
21. Stooge Sort - a recursive sorting algorithm with a bad time complexity.
22. Tim Sort - hybrid sorting algorithm derived from merge sort and insertion sort.
23. Strand Sort - recursively splits the list into sorted and unsorted parts.
24. Cube Sort - divides the list into sublists and then sorts them.
25. Odd-Even Sort - compares all odd/even indexed pairs of adjacent elements and swaps them
 if they are in the wrong order.
26. Quick Sort on Singly Linked List - sorts the list by selecting a pivot and partitioning the list.
27. Quick Sort on Doubly Linked List - sorts the list by selecting a pivot and partitioning the list.

O(nlogn) - the time complexity of most efficient sorting algorithms.

Stable Sorting - the relative order of equal elements is preserved in the sorted output.
Example: Bubble Sort, Merge Sort, Counting Sort, Radix Sort, Bucket Sort, Shell Sort, Cocktail Sort, 
Comb Sort, Pigeonhole Sort, Cycle Sort, Bitonic Sort, Pancake Sorting, Binary Insertion Sort, Gnome Sort, 
Sleep Sort, Strand Sort, Cube Sort, Odd-Even Sort, Quick Sort on Singly Linked List, 
Quick Sort on Doubly Linked List.

Unstable Sorting - the relative order of equal elements is not preserved in the sorted output.
Example: Selection Sort, Insertion Sort, Quick Sort, Heap Sort.

*/
